package ejercicios;

public class Ej1 {

	public static void main(String[] args) {
		int N = 8;
		double A = 20.8;
		char C = 'g';
		
		System.out.println("N es: " + N);
		System.out.println("A es: " + A);
		System.out.println("C es: " + C);
		System.out.println("A+N es: " + (A+N));
		System.out.println("A-N es: " + (A-N));

	}

}
