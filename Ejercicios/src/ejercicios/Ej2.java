package ejercicios;

public class Ej2 {

	public static void main(String[] args) {
		int X = 2;
		int Y = 3;
		double N = 4.2;
		double M = 5.4;
		
		System.out.println("X es: " + X);
		System.out.println("Y es: " + Y);
		System.out.println("N es: " + N);
		System.out.println("M es: " + M);
		System.out.println("X+Y es: " + (X+Y));
		System.out.println("Y-X es: " + (Y-X));
		System.out.println("N+M es: " + (N+M));
		System.out.println("M-N es: " + (M-N));
		System.out.println("X+N es: " + (X+N));
		System.out.println("M-X es: " + (M-X));

	}

}
